    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
        double canhA = Double.parseDouble(jTextField8.getText());
        double canhB = Double.parseDouble(jTextField6.getText());
        double canhC = Double.parseDouble(jTextField3.getText());
        if (canhA <= 0 || canhB <= 0 || canhC <= 0) {
            JOptionPane.showMessageDialog(this, "Các cạnh phải lớn hơn 0. Vui lòng nhập lại.");
            return;
        }

        if (canhA + canhB <= canhC || canhA + canhC <= canhB || canhB + canhC <= canhA) {
            JOptionPane.showMessageDialog(this, "Tổng độ dài hai cạnh phải lớn hơn cạnh còn lại. Vui lòng nhập lại.");
            return;
        }
        double p = (canhA + canhB + canhC) / 2;
        double dienTich = Math.sqrt(p * (p - canhA) * (p - canhB) * (p - canhC));
        jTextField10.setText(String.format("%.2f", dienTich));
        jTextField10.setEnabled(false);


    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Dữ liệu nhập không hợp lệ. Vui lòng nhập lại bằng số.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

