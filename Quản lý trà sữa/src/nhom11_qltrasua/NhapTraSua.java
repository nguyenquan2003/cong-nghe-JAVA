    private void ShowDuLieu() {
        try {
            tbdanhsachsp.removeAll();
            String[] arr = {"Mã Trà Sữa", "Tên Trà Sữa", "Giá Bán", "Nhà Cung Cấp", "Giá Nhập"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);

            Connection connection = KetNoi.connectToDatabase();
            String query = "select TRASUA.MATRASUA, TRASUA.TENTRASUA, TRASUA.GiaBan, TRASUA.GiaNhap, NhaCungCap.TenNCC,TRASUA.IdHinh from TRASUA JOIN NhaCungCap on TRASUA.MaNCC = NhaCungCap.MaNCC";
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Vector vector = new Vector(); 
                vector.add(rs.getString("MATRASUA"));
                vector.add(rs.getString("TENTRASUA"));
                vector.add(rs.getString("GiaBan"));  
                vector.add(rs.getString("TenNCC"));
                vector.add(rs.getString("GiaNhap"));
                vector.add(rs.getBytes("IdHinh"));
                lsTS.add(new TraSua(rs.getString("MATRASUA"), rs.getString("TENTRASUA"),rs.getFloat("GiaBan"),rs.getFloat("GiaNhap"),rs.getString("TenNCC") , rs.getString("IdHinh")));
                model.addRow(vector);
            }
            tbdanhsachsp.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(NhapTraSua.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
