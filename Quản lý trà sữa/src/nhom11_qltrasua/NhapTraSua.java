     public String taoMaTraSua()
     {
         String idTraSua = "";
        try
        {
             Connection con;
            con = KetNoi.connectToDatabase();
            String sql= "select count(*) from TRASUA";
            
            Statement st = (Statement) con.createStatement();
            ResultSet result = st.executeQuery(sql);
            
            int number = 0;
            while (result.next())
            {
                 number  = result.getInt(1);
               
            }
             if (number >= 0 && number < 10)
                idTraSua = "TS00" + String.valueOf(number + 1);
            else if (number >= 10 && number < 100) 
                idTraSua ="TS0" + String.valueOf(number +1);
            else if (number >= 100 && number < 1000)
                idTraSua = "TS" + String.valueOf(number + 1);
            else
                JOptionPane.showMessageDialog(null, "Dữ liệu đã đầy. Vui lòng liên hệ nhà phát triển để tăng ô chứa dữ liệu!");
        } catch (SQLException ex) {
             Logger.getLogger(DatTraSuaController.class.getName()).log(Level.SEVERE, null, ex);
         }
        return idTraSua;
     }
    
    
     
