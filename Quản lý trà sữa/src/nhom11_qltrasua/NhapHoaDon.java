    private float layTongTien(String maHoaDonBan)
    {
        float tongTien = 0;
      
        try
        {
              Connection connection = KetNoi.connectToDatabase();
            String sql = "select TongTienBan from HoaDonBan where MaHDB = '"
+maHoaDonBan+"'";  
        Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(sql);
        if(rs.next())
        {
            tongTien = rs.getFloat("TongTienBan");
        }
        } catch (SQLException ex) {
            Logger.getLogger(NhapHoaDon.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return tongTien;
    }
    
    public  double tinhTongTien(float tienKhachDua) {
        float tienTra = 0;
    
       float tongTien = layTongTien(lblMaHDB.getText());
       tienTra = tienKhachDua - tongTien;
       return tienTra;
        
    }
    
    
