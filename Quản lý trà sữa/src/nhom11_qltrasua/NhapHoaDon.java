    private void ShowDuLieu() {
        try {
            tbchitiethd.removeAll();
            String[] arr = {"Mã sản phẩm", "Tên sản phẩm", "Số lượng", "Giá bán", "Thành tiền"};
            DefaultTableModel model = new DefaultTableModel(arr, 0);

            Connection connection = KetNoi.connectToDatabase();
            String query = "select TRASUA.MATRASUA, TRASUA.TENTRASUA, TRASUA.GiaBan, ChiTietHoaDon.SoLuong, ChiTietHoaDon.ThanhTien from TRASUA JOIN ChiTietHoaDon on  ChiTietHoaDon.MATRASUA = TRASUA.MATRASUA and ChiTietHoaDon.MaHDB = '" +lblMaHDB.getText()+"'";
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("MATRASUA"));
                vector.add(rs.getString("TENTRASUA"));
                vector.add(rs.getString("GiaBan"));
                vector.add(rs.getString("SoLuong"));
                vector.add(rs.getString("ThanhTien"));
                model.addRow(vector);
            }
            tbchitiethd.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(NhapHoaDon.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    

