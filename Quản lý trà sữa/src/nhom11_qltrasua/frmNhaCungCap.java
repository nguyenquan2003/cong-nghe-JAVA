    private void makeNhaCungCapId() 
    {
        try
        {
            String user="sa";
            String pass="123";
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://DESKTOP-KSEC4J7\\SQLEXPRESS:1433;databaseName=QL_QUAN_TRA_SUA";
            Connection con;
            con = DriverManager.getConnection(url, user,pass);
            
            String sql= "select count(*) from NhaCungCap";
            
            Statement st = (Statement) con.createStatement();
            ResultSet result = st.executeQuery(sql);
            
            int number = 0;
            while (result.next())
            {
                 number  = result.getInt(1);
               
            }

            if (number >= 0 && number < 10)
                txtMaNhaCungCap.setText("S000" + String.valueOf(number + 1));
            else if (number >= 10 && number < 100) 
                txtMaNhaCungCap.setText("S00" + String.valueOf(number +1));
            else if (number >= 100 && number < 1000)
                txtMaNhaCungCap.setText("S0" + String.valueOf(number + 1));
            else if (number >= 1000 && number < 10000)
                txtMaNhaCungCap.setText("S" + String.valueOf(number +1));
            else
                JOptionPane.showMessageDialog(null, "Hóa đơn trong cơ sở dữ liệu đã đầy. Vui lòng liên hệ nhà phát triển để tăng ô chứa dữ liệu!");
        } catch (SQLException ex) {
            Logger.getLogger(frmNhaCungCap.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmNhaCungCap.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }
    
