    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
       
              try 
        {
            for(char c : txtSoDienThoai.getText().toCharArray())
            {
               
             if(!Character.isDigit(c))
             { JOptionPane.showMessageDialog(null, "Số điện thoại phải là số");
                             txtSoDienThoai.requestFocus();
             return;
             }
            }
            
            if(!(txtSoDienThoai.getText().length() == 10))
            {
                JOptionPane.showMessageDialog(null, "Số điện thoại phải 10 số");
                txtSoDienThoai.requestFocus();
                return;
            }
             String soDau = txtSoDienThoai.getText().substring(0, 1);
             if (Integer.parseInt(soDau) != 0)
             {
                 JOptionPane.showMessageDialog(null, "Đầu số điện thoại phải là 0");
                 txtSoDienThoai.requestFocus();
                return;
             }
            
            
           Connection con = KetNoi.connectToDatabase();
            String sql= "insert into NhaCungCap values (?,?,?,?)";
            PreparedStatement st =  con.prepareStatement(sql);
             
//            if(con != null)
//                JOptionPane.showMessageDialog(null, "kết nối thành công");
            
            st.setString(1,txtMaNhaCungCap.getText());
            st.setString(2,txtTenNhaCungCap.getText());
            st.setString(3,txtSoDienThoai.getText());
            st.setInt(4, 1);
            st.executeUpdate();
            st.close();
//            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Thêm thành công.");
            con.close();
            clearText();
           
        } catch (SQLException ex) {
            Logger.getLogger(frmDanhSachTraSua.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        tblDsNhaCungCap.clearSelection();
        loadNhaCungCap();
    }//GEN-LAST:event_btnThemActionPerformed

