      private void LoadDataToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        ArrayList<SinhVienPOJO> listSV = DAO_SinhVien.getInstance().ShowAll();
        for (SinhVienPOJO sv : listSV) {
            model.addRow(new Object[]{sv.getId(), sv.getFirstName(), sv.getLastName(), sv.getGender(), sv.getPhoneNumber(), sv.getBirthday()});
        }
    }
      private void showSelectedRowData(JTable table) {
    int selectedRowIndex = table.getSelectedRow();
    if (selectedRowIndex >= 0) {
        txt_masv.setText(table.getValueAt(selectedRowIndex, 0).toString());
        txt_firstName.setText(table.getValueAt(selectedRowIndex, 1).toString());
        txt_lastName.setText(table.getValueAt(selectedRowIndex, 2).toString());
        // Hiển thị giới tính
        String gender = table.getValueAt(selectedRowIndex, 3).toString();
        cbo_gioitinh.setSelectedItem(gender);
        txt_phone.setText(table.getValueAt(selectedRowIndex, 4).toString());
        // Hiển thị ngày sinh
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date birthday;
        try {
            birthday = sdf.parse(table.getValueAt(selectedRowIndex, 5).toString());
            txt_NgaySinh.setText(sdf.format(birthday));
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
    }
}
      
      
