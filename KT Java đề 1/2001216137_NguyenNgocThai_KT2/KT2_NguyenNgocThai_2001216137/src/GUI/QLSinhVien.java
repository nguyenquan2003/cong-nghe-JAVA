    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
          int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        SinhVienPOJO svToDelete = new SinhVienPOJO();
        svToDelete.setId((int) jTable1.getValueAt(selectedRow, 0));
        int result = DAO_SinhVien.getInstance().Xoa(svToDelete);
        if (result > 0) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "Đã xóa sinh viên thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Xóa sinh viên thất bại!");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một sinh viên để xóa!");
    }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
     String firstName = txt_firstName.getText();
    String lastName = txt_lastName.getText();
    String gender = cbo_gioitinh.getSelectedItem().toString();
    String phoneNumber = txt_phone.getText();
    String birthdayString = txt_NgaySinh.getText();

    // Chuyển chuỗi ngày sinh sang định dạng Date
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    Date birthday = null;
    try {
        birthday = sdf.parse(birthdayString);
    } catch (ParseException ex) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày sinh theo định dạng yyyy-MM-dd", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return; // Thoát khỏi phương thức nếu ngày sinh không hợp lệ
    }
    SinhVienPOJO svToAdd = new SinhVienPOJO(0, firstName, lastName, gender, phoneNumber, birthday);
   
    // Thêm sinh viên mới vào cơ sở dữ liệu
int result = DAO_SinhVien.getInstance().Them(svToAdd);

// Kiểm tra kết quả thêm sinh viên
if (result > 0) {
    // Nếu thành công, cập nhật bảng với sinh viên mới
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.addRow(new Object[]{svToAdd.getId(), svToAdd.getFirstName(), svToAdd.getLastName(), svToAdd.getGender(), svToAdd.getPhoneNumber(), svToAdd.getBirthday()});
    JOptionPane.showMessageDialog(this, "Đã thêm sinh viên thành công!");
} else {
    // Nếu thất bại, hiển thị thông báo
    JOptionPane.showMessageDialog(this, "Thêm sinh viên thất bại!");
}
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_capnhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_capnhapActionPerformed
      int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        int studentId = Integer.parseInt(txt_masv.getText());
        String firstName = txt_firstName.getText();
        String lastName = txt_lastName.getText();
        String gender = cbo_gioitinh.getSelectedItem().toString();
        String phoneNumber = txt_phone.getText();
        java.util.Date birthday = null;
          try {
              birthday = new SimpleDateFormat("yyyy-MM-dd").parse(txt_NgaySinh.getText());
          } catch (ParseException ex) {
              Logger.getLogger(QLSinhVien.class.getName()).log(Level.SEVERE, null, ex);
          }
        
        SinhVienPOJO svToUpdate = new SinhVienPOJO(studentId, firstName, lastName, gender, phoneNumber, birthday);
        int result = DAO_SinhVien.getInstance().Sua(svToUpdate);
        if (result > 0) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setValueAt(studentId, selectedRow, 0);
            model.setValueAt(firstName, selectedRow, 1);
            model.setValueAt(lastName, selectedRow, 2);
            model.setValueAt(gender, selectedRow, 3);
            model.setValueAt(phoneNumber, selectedRow, 4);
            model.setValueAt(birthday, selectedRow, 5);
            JOptionPane.showMessageDialog(this, "Đã cập nhật sinh viên thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật sinh viên thất bại!");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một sinh viên để cập nhật!");
    }
    }//GEN-LAST:event_btn_capnhapActionPerformed

