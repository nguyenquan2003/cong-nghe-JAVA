    private void displaySanPhamList(String selectedDanhMuc) throws SQLException {
        List<SanPham> sanPhamList = sanPhamBusiness.getSanPhamByDanhMuc(selectedDanhMuc);
        DefaultTableModel model = new DefaultTableModel();
        model.setRowCount(0);
        model.addColumn("Mã Sản Phẩm");
        model.addColumn("Tên Sản Phẩm");
        model.addColumn("Số lượng");
        model.addColumn("Đơn giá");
        for (SanPham sanPham : sanPhamList) {
            model.addRow(new Object[]{sanPham.getMaSP(), sanPham.getTenSP(), sanPham.getSoLuong(), sanPham.getDonGia()});
        }
        tblSP.setModel(model);
    }
    private void listDMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listDMMouseClicked
        // TODO add your handling code here:
        String selectedDanhMuc = listDM.getSelectedValue();
        if (selectedDanhMuc != null) {
            try {
                String ma = danhMucBusiness.getMaDanhMucByTenDanhMuc(selectedDanhMuc);
                displaySanPhamList(ma);
                cbbDM.setSelectedItem(selectedDanhMuc);
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi khi hiển thị sản phẩm", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_listDMMouseClicked

    private void btnThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSPActionPerformed
        // TODO add your handling code here:
        try {
            String ma = txtMA.getText();
            String ten = txtTen.getText();
            int soLuong = Integer.parseInt(txtSoLuong.getText());
            float donGia = Float.parseFloat(txtDonGia.getText());
            String maDM = danhMucBusiness.getMaDanhMucByTenDanhMuc(cbbDM.getSelectedItem().toString());
            SanPham sp = new SanPham(ma, ten, soLuong, donGia, maDM, true);
            sanPhamBusiness.addSanPham(sp);
            displaySanPhamList(maDM);
        } catch (SQLException ex) {
            Logger.getLogger(QL_SanPham.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemSPActionPerformed

    private void btnCNSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCNSPActionPerformed
        // TODO add your handling code here:

        try {
            String ma = txtMA.getText();
            String ten = txtTen.getText();
            int soLuong = Integer.parseInt(txtSoLuong.getText());
            float donGia = Float.parseFloat(txtDonGia.getText());
            String maDM = danhMucBusiness.getMaDanhMucByTenDanhMuc(cbbDM.getSelectedItem().toString());
            SanPham sp = new SanPham(ma, ten, soLuong, donGia, maDM, true);
            sanPhamBusiness.updateSanPham(sp);
            displaySanPhamList(maDM);
        } catch (SQLException ex) {
            Logger.getLogger(QL_SanPham.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnCNSPActionPerformed

    private void tblSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSPMouseClicked
        // TODO add your handling code here:
        int row = tblSP.getSelectedRow();
        txtMA.setText((String) tblSP.getValueAt(row, 0));
        txtTen.setText((String) tblSP.getValueAt(row, 1));
        txtSoLuong.setText(tblSP.getValueAt(row, 2).toString());
        txtDonGia.setText(tblSP.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblSPMouseClicked

    private void btnXoaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaSPActionPerformed
        // TODO add your handling code here:
        String ma = txtMA.getText();
        try {
            sanPhamBusiness.deleteSanPham(ma);
            String maDM = danhMucBusiness.getMaDanhMucByTenDanhMuc(cbbDM.getSelectedItem().toString());
            displaySanPhamList(maDM);
        } catch (SQLException ex) {
            Logger.getLogger(QL_SanPham.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnXoaSPActionPerformed

