    private void displayDanhMucList() {
        DefaultListModel<String> model = new DefaultListModel<>();
        try {
            List<DanhMuc> danhMucListData = danhMucBusiness.getAllDanhMuc();
            for (DanhMuc danhMuc : danhMucListData) {
                model.addElement(danhMuc.getTenDM());
                cbbDM.addItem(danhMuc.getTenDM());
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi lấy dữ liệu từ cơ sở dữ liệu", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        // Set model cho JList
        listDM.setModel(model);
    }

